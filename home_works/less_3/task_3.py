"""
3. Реализовать функцию my_func(), которая принимает три позиционных аргумента,
и возвращает сумму наибольших двух аргументов.
"""
def my_func(a,b,c):
    """
    func принимает три позиционных аргумента,
    и возвращает сумму наибольших двух аргументов.
    :param a: int
    :param b: int
    :param c: int
    :return: int
    """
    min_num = min(a,b,c)
    return (a+b+c)-min_num
